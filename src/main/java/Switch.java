
public class Switch {
	public static void main(String[] arg)
	{
		int a=0;
		switch(a)
		{
		case(0):System.out.println("a is value 0");
		       
		case(5):System.out.println("a is value 5");
		       
		case(7):System.out.println("a is value 7");
		       
		default:System.out.println("a does not matching");
		
		}
	}

}
